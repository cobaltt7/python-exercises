from functools import reduce


def ninetyOne(data):
    """Write a Python program to find all n-digit integers that start or end with 2."""
    return [
        number
        for number in range(10 ** (data - 1), 10**data)
        if str(number).startswith("2") or str(number).endswith("2")
    ]


