def ninetyTwo(data):
    """
    Write a Python program to start with a list of integers, keep every other element in place and
    otherwise sort the list.
    """
    arrays = reduce(
        lambda acc, entry: acc[entry[0] % 2].append(entry[1]) or acc,
        enumerate(data),
        [[], []],
    )
    arrays[0] = sorted(arrays[0])
    coalesced = [
        (array[i] if i < len(array) else None)
        for i in range(len(arrays[0]))
        for array in arrays
    ]
    return [x for x in coalesced if x is not None]


print(ninetyTwo([2, 5, 6, 3, 1, 4, 34]))
assert ninetyTwo([2, 5, 6, 3, 1, 4, 34]) == [1, 5, 2, 3, 6, 4, 34]
assert ninetyTwo([8, 0, 7, 2, 9, 4, 1, 2, 8, 3]) == [1, 0, 7, 2, 8, 4, 8, 2, 9, 3]
